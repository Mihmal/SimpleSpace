public class FlyObject 
{

  public string name
  public double mass
  public double x
  public double y
  public double vx
  public double vy
  
  public FlyObject(string newName, double newMass, double newX, double newY, double newVx, double newVy) {
    name = newName
    mass = newMass
    x = newX
    y = newY
    vx = newVx
    vy = newVy
  }
}
